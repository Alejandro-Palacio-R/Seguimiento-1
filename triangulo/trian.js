let ang1 = parseInt(prompt("Ingrese el valor del primer angulo"))
let ang2 = parseInt(prompt("Ingrese el valor del egundo angulo"))
let ang3 = parseInt(prompt("Ingrese el valor del tercer angulo"))

let suma = ang1 + ang2 + ang3 

if(suma == 180){
    alert("El triangulo es valido")
}
else{
    alert("el triangulo no es valido")
}